void main() {
  //수식
  print('hello world');
  print('hahaha');
  print(2.5 / 2); // 1.35
  print(2.5 * 2);
  print(2.5 + 2);
  print(2.5 % 2);
  print('2.5 - 2 = ${2.5 - 2}'); //글 사이에 수식을 넣을 때는 'text ${수식}'
  print(2.5 / 2);

  //변수
  int x;  //integer 정수 변수 선언(declare)
  x = 10; // 번수 x에 10을 대입(assign)

  int x = 10; // 선언과 동시에 대입(초기화)
  x = 20; //변수 재 할당은 곧 안될 예정
  print('안녕하세요. 저는 ${x - 5 * 2}살 입니다.'); // 수식을 대입 가능
}
